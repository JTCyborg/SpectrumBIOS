;=========================================================
;
; FUZIX Boot Information Block
;
; SBIOS -> FUZIX
;
;=========================================================

BOOTINFO:

BI_Magic

        db "FUZIX",0

BI_Version

        dw 1

;---------------------------------------------------------
; RAM
;---------------------------------------------------------

BI_RAM_Base

        dw #0000

BI_RAM_Size

        dw 1024        ; KB

;---------------------------------------------------------
; ROM
;---------------------------------------------------------

BI_ROM_Base

        dw #0000

BI_ROM_Size

        dw 16

;---------------------------------------------------------
; Console
;---------------------------------------------------------

BI_Console

        db 1

;---------------------------------------------------------
; Keyboard
;---------------------------------------------------------

BI_Keyboard

        db 1

;---------------------------------------------------------
; IDE
;---------------------------------------------------------

BI_IDE

        db 1

BI_IDE_Master

        db 1

BI_IDE_Slave

        db 0

;---------------------------------------------------------
; RTC
;---------------------------------------------------------

BI_RTC

        db 0

;---------------------------------------------------------
; Video
;---------------------------------------------------------

BI_ScreenWidth

        db 32

BI_ScreenHeight

        db 24

BI_FontHeight

        db 8

;---------------------------------------------------------
; Kernel
;---------------------------------------------------------

BI_KernelAddress

        dw #C000

BI_KernelSize

        dw 0

;---------------------------------------------------------
; Root Device
;---------------------------------------------------------

BI_RootDevice

        db 0

;---------------------------------------------------------
; Boot Device
;---------------------------------------------------------

BI_BootDevice

        db 0

;---------------------------------------------------------
; Flags
;---------------------------------------------------------

BI_Flags

        dw 0

;---------------------------------------------------------
; Command Line
;---------------------------------------------------------

BI_CommandLine

        dw BootCommandLine

;---------------------------------------------------------

BootCommandLine

db "root=/dev/hda1 console=tty0 rw",0