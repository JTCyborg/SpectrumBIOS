EXT2_MAGIC             EQU #EF53

;---------------------------------------------------------
; Variables
;---------------------------------------------------------

EXT2_BlockSize

        dw 1024

EXT2_InodeSize

        dw 128

EXT2_FirstDataBlock

        dw 1

EXT2_InodesPerGroup

        dw 0

EXT2_BlocksPerGroup

        dw 0

EXT2_InodeTable

        dw 0

EXT2_LastError

        db 0

;---------------------------------------------------------
; Init
;---------------------------------------------------------

EXT2_Init

        call EXT2_ReadSuperBlock

        call EXT2_ParseSuperBlock

        ret

;---------------------------------------------------------
; Read SuperBlock
;---------------------------------------------------------

EXT2_ReadSuperBlock

        ld hl,EXT2_Buffer

        ld bc,2
        ld de,0

        call IDE_ReadSector

        ret

;---------------------------------------------------------
; Parse SuperBlock
;---------------------------------------------------------

EXT2_ParseSuperBlock

        ld hl,(EXT2_Buffer+56)

        ld a,l
        cp LOW EXT2_MAGIC
        jr nz,.error

        ld a,h
        cp HIGH EXT2_MAGIC
        jr nz,.error

        ld hl,(EXT2_Buffer+24)
        ld (EXT2_FirstDataBlock),hl

        ld hl,(EXT2_Buffer+40)
        ld (EXT2_InodesPerGroup),hl

        ld hl,(EXT2_Buffer+32)
        ld (EXT2_BlocksPerGroup),hl

        xor a
        ld (EXT2_LastError),a

        ret

.error

        ld a,1
        ld (EXT2_LastError),a

        ret

;---------------------------------------------------------
; Open
;---------------------------------------------------------

