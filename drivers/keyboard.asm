;=========================================================
;
; SBIOS
; keyboard.asm
;
; Keyboard Driver v0.0.1
;
; Pentagon / Spectrum 128
;
;=========================================================

        INCLUDE "inc/bios.inc"
        INCLUDE "inc/ports.inc"

;---------------------------------------------------------
; Variables
;---------------------------------------------------------

KeyCode         db 0

;---------------------------------------------------------
; Init
;---------------------------------------------------------

KEYBOARD_Init:

        xor     a
        ld      (KeyCode),a

        ret

;---------------------------------------------------------
; Status
;
; Z = no key
; NZ = key available
;---------------------------------------------------------

KEYBOARD_Status:

        ld      a,(KeyCode)

        or      a

        ret

;---------------------------------------------------------
; Read
;
; A = ASCII
;---------------------------------------------------------

KEYBOARD_Read:

        call    KEYBOARD_Scan

        ld      a,(KeyCode)

        xor     a
        ld      (KeyCode),a

        ld      a,(KeyCode)

        ret

;---------------------------------------------------------
; Wait
;---------------------------------------------------------

KEYBOARD_Wait:

.loop

        call    KEYBOARD_Scan

        ld      a,(KeyCode)

        or      a

        jr      z,.loop

        ret

;---------------------------------------------------------
; Scan keyboard
;
; v0.0.1
; Только SPACE
;---------------------------------------------------------

KEYBOARD_Scan:

        ld      bc,#7FFE

        in      a,(c)

        bit     0,a

        jr      nz,.nokey

        ld      a,' '

        ld      (KeyCode),a

        ret

.nokey

        xor     a

        ld      (KeyCode),a

        ret

;---------------------------------------------------------
; Flush
;---------------------------------------------------------

KEYBOARD_Flush:

        xor     a

        ld      (KeyCode),a

        ret

;---------------------------------------------------------
; Get last key
;---------------------------------------------------------

KEYBOARD_Last:

        ld      a,(KeyCode)

        ret