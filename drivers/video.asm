        INCLUDE "inc/bios.inc"
        INCLUDE "inc/ports.inc"

;---------------------------------------------------------
; Init Video
;---------------------------------------------------------

VIDEO_Init:

        xor     a
        out     (PORT_FE),a

        ret

;---------------------------------------------------------
; Border
;
; A = color
;---------------------------------------------------------

VIDEO_SetBorder:

        and     7
        out     (PORT_FE),a

        ret

;---------------------------------------------------------
; Clear Screen
;---------------------------------------------------------

VIDEO_Clear:

        ld      hl,#4000
        ld      de,#4001
        ld      bc,6143

        ld      (hl),0
        ldir

        ld      hl,#5800
        ld      de,#5801
        ld      bc,767

        ld      (hl),7
        ldir

        ret

;---------------------------------------------------------
; Fill Attributes
;
; A = attribute
;---------------------------------------------------------

VIDEO_FillAttr:

        ld      hl,#5800
        ld      de,#5801
        ld      bc,767

        ld      (hl),a

        ldir

        ret

;---------------------------------------------------------
; Draw Character
;
; IN:
;   A = ASCII
;   B = X (0..31)
;   C = Y (0..23)
;
; v0.0.1
; Пока заглушка.
;---------------------------------------------------------

VIDEO_DrawChar:

        ret

;---------------------------------------------------------
; Scroll Up
;---------------------------------------------------------

VIDEO_Scroll:

        ret

;---------------------------------------------------------
; Set Ink/Paper
;
; A = attribute
;---------------------------------------------------------

VIDEO_SetAttribute:

        ld      (VideoAttribute),a

        ret

;---------------------------------------------------------

VideoAttribute:

        db      7