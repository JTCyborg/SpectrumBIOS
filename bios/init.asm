;---------------------------------------------------------
; Clear BIOS Data Area
;---------------------------------------------------------

BIOS_ClearBDA:

        ld      hl,BDA_BASE
        ld      de,BDA_BASE+1
        ld      bc,255

        xor     a
        ld      (hl),a
        ldir

        ret

;---------------------------------------------------------
; CPU
;---------------------------------------------------------

BIOS_InitCPU:

        im      1

        xor     a
        ld      i,a

        ret

;---------------------------------------------------------
; Memory
;---------------------------------------------------------

BIOS_InitMemory:

        ld      hl,1024
        ld      (BDA_RAM_SIZE),hl

        ret

;---------------------------------------------------------
; Video
;---------------------------------------------------------

BIOS_InitVideo:

        xor     a
        out     (#FE),a

        xor     a
        ld      (BDA_CURSOR_X),a
        ld      (BDA_CURSOR_Y),a

        ret

;---------------------------------------------------------
; Keyboard
;---------------------------------------------------------

BIOS_InitKeyboard:

        xor     a
        ld      (BDA_KEY_FLAGS),a

        ret

;---------------------------------------------------------
; Timer
;---------------------------------------------------------

BIOS_InitTimer:

        xor     a

        ld      (BDA_TICKS),a
        ld      (BDA_TICKS+1),a
        ld      (BDA_TICKS+2),a
        ld      (BDA_TICKS+3),a

        ret

;---------------------------------------------------------
; RTC
;---------------------------------------------------------

BIOS_InitRTC:

        xor     a
        ld      (BDA_RTC_PRESENT),a

        ret

;---------------------------------------------------------
; IDE
;---------------------------------------------------------

BIOS_InitIDE:

        xor     a

        ld      (BDA_IDE_PRESENT),a
        ld      (BDA_MASTER_PRESENT),a
        ld      (BDA_SLAVE_PRESENT),a

        ret

;---------------------------------------------------------
; IRQ
;---------------------------------------------------------

BIOS_TimerIRQ:

        ld      hl,BDA_TICKS

        inc     (hl)
        ret     nz

        inc     hl
        inc     (hl)
        ret     nz

        inc     hl
        inc     (hl)
        ret     nz

        inc     hl
        inc     (hl)

        ret

;---------------------------------------------------------
; NMI
;---------------------------------------------------------

BIOS_NMI:

        ret