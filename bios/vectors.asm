        ; Продолжение после reset.asm

;---------------------------------------------------------
; RST 08 - Console
;---------------------------------------------------------

        DS      #0008-$,0

RST08:
        jp      BIOS_Console

;---------------------------------------------------------
; RST 10 - Keyboard
;---------------------------------------------------------

        DS      #0010-$,0

RST10:
        jp      BIOS_Keyboard

;---------------------------------------------------------
; RST 18 - Video
;---------------------------------------------------------

        DS      #0018-$,0

RST18:
        jp      BIOS_Video

;---------------------------------------------------------
; RST 20 - Disk
;---------------------------------------------------------

        DS      #0020-$,0

RST20:
        jp      BIOS_Disk

;---------------------------------------------------------
; RST 28 - Memory
;---------------------------------------------------------

        DS      #0028-$,0

RST28:
        jp      BIOS_Memory

;---------------------------------------------------------
; RST 30 - Timer
;---------------------------------------------------------

        DS      #0030-$,0

RST30:
        jp      BIOS_Timer

;---------------------------------------------------------
; RST 38 - System + IM1
;---------------------------------------------------------

        DS      #0038-$,0

RST38:

        push af
        push bc
        push de
        push hl
        push ix
        push iy

        call BIOS_TimerIRQ

        pop iy
        pop ix
        pop hl
        pop de
        pop bc
        pop af

        ei
        reti

;---------------------------------------------------------
; NMI
;---------------------------------------------------------

        DS      #0066-$,0

NMI_Handler:

        push af

        call BIOS_NMI

        pop af

        retn

;---------------------------------------------------------
; BIOS code starts here
;---------------------------------------------------------

        DS      #0100-$,0